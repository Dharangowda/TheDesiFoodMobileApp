// Helper function to get subcategories for a category
export function getSubcategories(categoryName: string) {
    return SUBCATEGORIES[categoryName as keyof typeof SUBCATEGORIES] || [];
}

// Helper function to get all items for a category
export function getAllSubcategoryItems(categoryName: string) {
    const subcats = getSubcategories(categoryName);
    return subcats.flatMap(subcat => subcat.items);
}